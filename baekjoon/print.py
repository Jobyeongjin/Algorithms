# 12. 나머지
# 입력 : 첫째 줄에 A, B, C가 순서대로 주어짐 (2 ≤ A, B, C ≤ 10000)
# 출력 : 첫째 줄에 (A+B)%C, 둘째 줄에 ((A%C) + (B%C))%C, 셋째 줄에 (A×B)%C, 넷째 줄에 ((A%C) × (B%C))%C를 출력

# 숫자형으로 변환한 입력 값을 나눠 순차적으로 변수에 넣고
A, B, C = map(int, input().split())

# 출력하라는 값대로 넣고 한 줄씩 출력
# 곱하기 기호는 바꾸기
print((A+B) % C, ((A % C) + (B % C)) % C, (A*B) %
      C, ((A % C) * (B % C)) % C, sep='\n')


# 13. 곱셈
# 입력 : 첫째 줄에 (1)의 위치에 들어갈 세 자리 자연수(472), 둘째 줄에 (2)의 위치에 들어갈 세자리 자연수(385)
# 출력 : 첫째 줄부터 넷째 줄까지 차례대로 (3), (4), (5), (6)에 들어갈 값을 출력

A = int(input())
B = int(input())

print(A*(B % 10), A*(B % 100 // 10), A*(B//100), A*B, sep='\n')

# 13번 문제 해설용💡
# B를 10으로 나눈 나머지인 셋째자리 5
print(A*(B % 10))
# B를 100으로 나눈 나머지 85, 85를 다시 10으로 나눈 몫인 둘째자리 8
print(A*(B % 100 // 10))
# B를 100으로 나눈 몫 첫쨰자리 3
print(A*(B//100))
# A와 B를 곱한 값
print(A*B)
