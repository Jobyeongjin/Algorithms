# 5. A+B
# 입력 : 첫째 줄에 A와 B (0 < A, B < 10)
# 출력 : A+B

# 입력을 위해 input() 함수를 사용하고, 한 줄에 들어온 문자를 공백을 기준으로 나눠서 각 변수에 입력
# map() 함수를 사용하여 나눈 문자열을 숫자형으로 변환
